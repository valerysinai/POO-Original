public class CalcularCuadrado {
    public static void main(String[] args) {
        int num = 4;
        int cuadrado = num * num;
        int cubo = num * num * num;
        System.out.println("El cuadrado de " + num + " es: " + cuadrado);
        System.out.println("El cubo de " + num + " es: " + cubo);
    }
}
